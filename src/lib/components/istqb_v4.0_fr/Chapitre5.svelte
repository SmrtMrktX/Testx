<div class="mx-auto max-w-5xl space-y-6 rounded-2xl bg-white p-6 shadow-lg">
	<h1 class="text-3xl font-bold text-blue-600">Gestion des Activités de Test</h1>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Introduction</h2>
		<p class="text-gray-700">
			La gestion des activités de test est un processus essentiel qui permet de planifier,
			organiser, contrôler et suivre l’ensemble des tests pour garantir la qualité du logiciel.
		</p>
	</section>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Principales Activités de Gestion des Tests</h2>

		<h3 class="text-xl font-semibold text-gray-700">1. Planification des Tests</h3>
		<p class="text-gray-700">
			Définir les objectifs, la portée, les ressources et le calendrier des tests.
		</p>

		<h3 class="text-xl font-semibold text-gray-700">2. Estimation des Efforts de Test</h3>
		<p class="text-gray-700">
			Évaluer le temps et les ressources nécessaires pour exécuter les tests.
		</p>

		<h3 class="text-xl font-semibold text-gray-700">3. Suivi et Contrôle des Tests</h3>
		<p class="text-gray-700">
			Surveiller l’avancement des tests et ajuster en fonction des résultats.
		</p>

		<h3 class="text-xl font-semibold text-gray-700">4. Gestion des Défauts</h3>
		<p class="text-gray-700">
			Identifier, enregistrer, suivre et corriger les anomalies détectées.
		</p>

		<h3 class="text-xl font-semibold text-gray-700">5. Reporting et Communication</h3>
		<p class="text-gray-700">
			Produire des rapports sur la progression et la qualité du logiciel testé.
		</p>
	</section>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Outils de Gestion des Tests</h2>
		<ul class="list-disc space-y-2 pl-6 text-gray-700">
			<li><strong>JIRA</strong> : Outil de suivi des tests et gestion des défauts.</li>
			<li><strong>TestRail</strong> : Gestion des plans de test et des cas de test.</li>
			<li><strong>qTest</strong> : Plateforme de gestion de test agile.</li>
			<li><strong>Zephyr</strong> : Outil intégré à JIRA pour la gestion des tests.</li>
		</ul>
	</section>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Importance de la Gestion des Tests</h2>
		<p class="text-gray-700">
			Une bonne gestion des tests permet d’optimiser les ressources, de minimiser les risques, de
			garantir une traçabilité complète et d’améliorer l’efficacité globale du processus de test.
		</p>
	</section>
</div>

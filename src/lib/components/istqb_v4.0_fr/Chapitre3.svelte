<div class="mx-auto max-w-5xl space-y-6 rounded-2xl bg-white p-6 shadow-lg">
	<h1 class="text-3xl font-bold text-blue-600">Test Statique</h1>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Introduction</h2>
		<p class="text-gray-700">
			Le test statique est une méthode de vérification du logiciel qui permet de détecter les
			erreurs sans exécuter le code. Il comprend l'examen des documents, l'analyse du code et
			l'utilisation d'outils d'analyse statique.
		</p>
	</section>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Types de Tests Statiques</h2>

		<h3 class="text-xl font-semibold text-gray-700">1. Revue de Code</h3>
		<p class="text-gray-700">
			La revue de code est un processus où plusieurs développeurs analysent le code source pour
			identifier les erreurs et améliorer la qualité.
		</p>

		<h3 class="text-xl font-semibold text-gray-700">2. Analyse Statique</h3>
		<p class="text-gray-700">
			L'analyse statique est réalisée par des outils automatisés qui inspectent le code pour
			détecter des failles de sécurité, des erreurs de syntaxe ou des violations de bonnes
			pratiques.
		</p>

		<h3 class="text-xl font-semibold text-gray-700">3. Inspection</h3>
		<p class="text-gray-700">
			Une inspection est un processus formel où un groupe d'experts analyse le code et la
			documentation pour détecter des anomalies.
		</p>
	</section>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Avantages des Tests Statiques</h2>
		<ul class="list-disc space-y-2 pl-6 text-gray-700">
			<li>Détection précoce des erreurs, réduisant les coûts de correction.</li>
			<li>Amélioration de la qualité du code grâce à l’application de normes.</li>
			<li>Identification des vulnérabilités de sécurité avant l'exécution.</li>
			<li>Gain de temps en évitant l’exécution de tests longs et coûteux.</li>
		</ul>
	</section>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Outils de Test Statique</h2>
		<ul class="list-disc space-y-2 pl-6 text-gray-700">
			<li>
				<strong>SonarQube</strong> : Analyseur statique pour identifier les défauts et améliorer la qualité.
			</li>
			<li><strong>ESLint</strong> : Vérificateur de qualité de code pour JavaScript.</li>
			<li><strong>Checkstyle</strong> : Outil d’analyse de code Java.</li>
			<li><strong>FindBugs</strong> : Détecte les bogues potentiels dans le code Java.</li>
			<li><strong>Flake8</strong> : Vérification de style et d'erreurs pour Python.</li>
		</ul>
	</section>

	<section class="space-y-4">
		<h2 class="text-2xl font-semibold text-gray-800">Conclusion</h2>
		<p class="text-gray-700">
			Le test statique est une approche efficace pour détecter les erreurs dès les premières phases
			du développement. Son intégration permet d'améliorer la qualité du code, d'assurer la
			conformité aux normes et de réduire les coûts liés aux défauts logiciels.
		</p>
	</section>
</div>
